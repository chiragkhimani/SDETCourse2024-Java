package com.java.Revision;

public class ArithmeticOperator {
    public static void main(String[] args) {

//        %2  ---->ODD Even

        System.out.println(10%2);
        System.out.println(11%2);
        System.out.println(12%2);
        System.out.println(14%2);
        System.out.println(15 % 2);

//         %10 ---->Retrieve the last digit from Number
        System.out.println(" %10 ---->Retrieve the last digit from Number");
        System.out.println(101%10);
        System.out.println(114%10);
        System.out.println(128%10);
        System.out.println(148372%10);
        System.out.println(153827%10);

//         /10 ---->Remove the last digit from Number
        System.out.println("/10 ---->Remove the last digit from Number");
        System.out.println(101/10);
        System.out.println(114/10);
        System.out.println(128/10);
        System.out.println(148372/10);
        System.out.println(153827/10);


    }

}
