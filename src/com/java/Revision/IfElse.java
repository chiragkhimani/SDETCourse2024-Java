package com.java.Revision;

public class IfElse {
    public static void main(String[] args) {

        int a = 10;

        if (a == 0) {
            System.out.println("Zero");
        } else if (a > 0) {
            System.out.println("Positive");
        } else {
            System.out.println("Negative");
        }


    }
}
