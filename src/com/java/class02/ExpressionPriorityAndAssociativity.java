package com.java.class02;

public class ExpressionPriorityAndAssociativity {
    public static void main(String[] args) {
        System.out.println(10 / 5 * 10);
        System.out.println(5 * 4 / 10);
        System.out.println(10 / 5 * 2 + 3 / 6 * 8 - 6 * 4 / 8);
        System.out.println(8 / 4 * 3 + 3 * 6 / 8 * 10 * 4 / 8);
    }
}