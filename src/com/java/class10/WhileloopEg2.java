package com.java.class10;

public class WhileloopEg2 {

    public static void main(String[] args) {
        //Initialization
        int i = 1;

        //Condition
        while (i<=5) {

            System.out.println(i); //1,3,5
            i++;
            i++;
        }

        // i = i+1 == i++ --> Increment Operator
        //i = i-1  == i-- -->  Decrement Operator

    }

}
